const router = require("express").Router();
const sequelize = require("../../config/connection");
const { Forum_Post, User, Forum_Comment } = require("../../models");
const withAuth = require("../../utils/auth");
const withAdmin = require("../../utils/admin");

//Gets all Forum Posts for the Forums Page
router.get("/", (req, res) => {
  console.log("======================");
  Forum_Post.findAll({
    include: [
      {
        model: Forum_Comment,
        attributes: ["id", "comment_text", "post_id", "user_id", "created_at"],
        include: {
          model: User,
          attributes: ["username"],
        },
      },
      {
        model: User,
        attributes: ["username"],
      },
    ],
  })
    .then((dbPostData) => res.json(dbPostData))
    .catch((err) => {
      console.log(err);
      res.status(500).json(err);
    });
});

//Gets one Forum Poart when selected
router.get("/:id", (req, res) => {
  Forum_Post.findOne({
    where: {
      id: req.params.id,
    },
    include: [
      {
        model: Forum_Comment,
        attributes: ["id", "comment_text", "post_id", "user_id", "created_at"],
        include: {
          model: User,
          attributes: ["username"],
        },
      },
      {
        model: User,
        attributes: ["username"],
      },
    ],
  })
    .then((dbPostData) => {
      if (!dbPostData) {
        res.status(404).json({ message: "No post found with this id" });
        return;
      }
      res.json(dbPostData);
    })
    .catch((err) => {
      console.log(err);
      res.status(500).json(err);
    });
});

//Creats a new Forum Post if logged in
router.post("/", withAuth, (req, res) => {
  Forum_Post.create({
    title: req.body.title,
    post_content: req.body.post_content,
    user_id: req.session.user_id,
  })
    .then((dbPostData) => res.json(dbPostData))
    .catch((err) => {
      console.log(err);
      res.status(500).json(err);
    });
});

//Updates a forum post by id
router.put("/:id", withAdmin, (req, res) => {
  Forum_Post.update(
    {
      title: req.body.title,
    },
    {
      where: {
        id: req.params.id,
      },
    }
  )
    .then((dbPostData) => {
      if (!dbPostData) {
        res.status(404).json({ message: "No post found with this id" });
        return;
      }
      res.json(dbPostData);
    })
    .catch((err) => {
      console.log(err);
      res.status(500).json(err);
    });
});

//Deletes a Forum Post
router.delete("/:id", withAdmin, (req, res) => {
  console.log("id", req.params.id);
  Forum_Post.destroy({
    where: {
      id: req.params.id,
    },
  })
    .then((dbPostData) => {
      if (!dbPostData) {
        res.status(404).json({ message: "No post found with this id" });
        return;
      }
      res.json(dbPostData);
    })
    .catch((err) => {
      console.log(err);
      res.status(500).json(err);
    });
});

module.exports = router;
